export function getTenantById(id: string): Tenant | undefined {
  return mockTenants.find((t) => t.id === id);
}

export function getTenantSummary(): TenantSummary {
  const total = mockTenants.length;
  const active = mockTenants.filter((t) => t.status === "active").length;
  const frozen = mockTenants.filter((t) => t.status === "frozen").length;
  const totalParkingLots = mockTenants.reduce((sum, t) => sum + t.parkingLotCount, 0);
  return {
    total,
    active,
    frozen,
    totalParkingLots,
    avgParkingLotsPerTenant: Number((totalParkingLots / total).toFixed(1)),
  };
}

export function getParkingLotsByTenantId(tenantId: string): ParkingLot[] {
  const lotIds = tenantParkingLotMap[tenantId] ?? [];
  return mockParkingLots.filter((lot) => lotIds.includes(lot.id));
}
